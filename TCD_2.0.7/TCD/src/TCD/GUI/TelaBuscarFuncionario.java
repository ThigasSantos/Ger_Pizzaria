    private void buscarTFKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscarTFKeyTyped

        String text = buscarTF.getText();
        char evtText = evt.getKeyChar();
        if(evtText != '\b' && evtText != '\n'){
            text = text + evtText;
        }

        funcionarios =  new FuncionarioDao().buscarPorNomeTelefone(text);
        str = new String[funcionarios.size()];
        for(int i = 0; i<funcionarios.size(); i++){
            str[i] = funcionarios.get(i).getNome().concat("  (" + funcionarios.get(i).getDDD() + ") "+ funcionarios.get(i).getTelefone());
        }
        jList1.setListData(str);

    }//GEN-LAST:event_buscarTFKeyTyped

