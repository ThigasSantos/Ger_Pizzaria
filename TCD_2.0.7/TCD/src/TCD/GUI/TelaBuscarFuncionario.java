    /**
     * Limpa os campos das text boxes
     */
    public void limparCampos(){
        nomeTF.setText("");
        dddTF.setText("");
        telefoneTF.setText("");
        cpfTF.setText("");
        cargoCB.setSelectedItem("");
        ruaTF.setText("");
        bairroTF.setText("");
        numeroTF.setText("");
        complementoTF.setText("");
    }
    
    /**
     * Atualiza a jlist de funcionarios
     */
    public void atualizarLista(){
        DefaultListModel model = new DefaultListModel();
        funcionarios.forEach(c->{
        model.addElement(c.toString());});
        jList1.setModel(model);
    }
    
    /**
     * Preenche os campos das text boxes com os valores de funcionario
     * @param func funcionario selecionado
     */
    public void preencherCampos(Funcionario func){
        nomeTF.setText(func.getNome());
        telefoneTF.setText(func.getTelefone());
        dddTF.setText(func.getDDD());
        cpfTF.setText(func.getCpf());
        cargoCB.setSelectedItem(func.getCargo());
        if(func.getEndereco() == null){
            ruaTF.setText("");        
            numeroTF.setText("");
            complementoTF.setText("");
            bairroTF.setText("");
        }
        else{
            ruaTF.setText(func.getEndereco().getRua());        
            numeroTF.setText(func.getEndereco().getNumero());
            complementoTF.setText(func.getEndereco().getComplemento());
            if(func.getEndereco().getBairro() == null)
                bairroTF.setText("");
            else
                bairroTF.setText(func.getEndereco().getBairro().getNome());
        }
        
    }
    
    /**
     * Busca todos os funcionarios no banco de dados
     */
    public void buscar(){
        funcionarios = new FuncionarioDao().localizarTodos();
        atualizarLista();
    }
    
    /**
     * Busca os funcionarios com base no filtro de nome e telefone
     * @param text nome ou telefone 
     */
    public void buscar(String text){
        funcionarios =  new FuncionarioDao().buscarPorNomeTelefone(text);
        atualizarLista();
    }

