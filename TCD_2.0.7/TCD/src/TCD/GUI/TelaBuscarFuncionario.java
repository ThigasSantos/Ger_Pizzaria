    private void atualizarBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarBTNActionPerformed
        Funcionario func;
        if(!jList1.isSelectionEmpty())
            func = funcionarios.get(jList1.getSelectedIndex());
        else
            func = new Funcionario();
        
        
        if(func.getEndereco()== null)
            func.setEndereco(new Endereco());
        if(pesquisaBairroList.getSelectedValue() == bairroTF.getText()){
            func.getEndereco().setBairro(bairrosList.get(pesquisaBairroList.getSelectedIndex()));
        }else{
            Bairro bairroTemp = new Bairro();
            bairroTemp.setNome(bairroTF.getText());
            bairroTemp.setId(new BairroDao().buscarExistente(bairroTemp));
            
            if(bairroTemp.getId() != null)
                func.getEndereco().setBairro(new BairroDao().localizarPorId(bairroTemp.getId()));
            else
                func.getEndereco().setBairro(bairroTemp);
                func.getEndereco().getBairro().setTaxa(0F);
                
            
        func.getEndereco().getBairro().setId(new BairroDao().salvar(func.getEndereco().getBairro()));    
        
        func.getEndereco().setRua(ruaTF.getText());
        func.getEndereco().setNumero(numeroTF.getText());
        func.getEndereco().setComplemento(complementoTF.getText());
        
        func.getEndereco().setId(new EnderecoDao().salvar(func.getEndereco()));
        
        func.setNome(nomeTF.getText());
        func.setDDD(dddTF.getText());
        func.setTelefone(telefoneTF.getText());
        func.setCpf(cpfTF.getText().replace("-", ""));
        func.setCargo((String)cargoCB.getSelectedItem());

        new FuncionarioDao().salvar(func);
        }
        buscar(buscarTF.getText());
        
        limparCampos();
    }//GEN-LAST:event_atualizarBTNActionPerformed

