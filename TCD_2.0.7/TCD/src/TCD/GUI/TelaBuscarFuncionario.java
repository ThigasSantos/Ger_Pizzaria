    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jList1.getSelectedValue() != null){
            Funcionario func = funcionarios.get(jList1.getSelectedIndex());
            new FuncionarioDao().excluir(func);
            if(func.getEndereco() != null)
                if(new EnderecoDao().verificarUso(func.getEndereco()) == false)
                    new EnderecoDao().excluir(func.getEndereco());
            buscar();
            limparCampos();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void cpfTFKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cpfTFKeyTyped
        String caracteresDisponiveis = "0123456789";
        
        if (!caracteresDisponiveis.contains(evt.getKeyChar() + "") || cpfTF.getText().length() >= 12) 
            evt.consume();
        else{
            String text = cpfTF.getText()+ evt.getKeyChar();
            if(text.length() == 9){
                cpfTF.setText(text + "-");
                evt.consume();
            }
        }
            
    }//GEN-LAST:event_cpfTFKeyTyped

