    /**
     * Busca todas as categorias
     */
    private void buscarTodos(){
        categorias = new CategoriaDao().localizarTodos();
        atualizarLista();
    }
    
    /**
     * Busca a categoria com base no filtro de nome
     * @param nome para filtro
     */
    private void buscarTodos(String nome){
        categorias = new CategoriaDao().buscarPorNome(nome);
        atualizarLista();
    }
    
    /**
     * Atualiza a jlist com as informações da busca
     */
    private void atualizarLista(){
        DefaultListModel model = new DefaultListModel();
        categorias.forEach(cat->{
            model.addElement(cat.getNome());
        });
        categoriasList.setModel(model);
    }
