    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        String text = jTextField1.getText();
        char evtText = evt.getKeyChar();
        if(evtText != '\b' && evtText != '\n'){
            text = text + evtText;
        }
        buscarTodos(text);
    }//GEN-LAST:event_jTextField1KeyTyped

    private void categoriasListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_categoriasListMouseClicked
        if(categoriasList.getSelectedValue() == null )
            return;
        novaCategoria = categorias.get(categoriasList.getSelectedIndex());

        nomeTF.setText(novaCategoria.getNome());
        jTextField2.setText(Float.toString(novaCategoria.getPreco()[0]));
        jTextField3.setText(Float.toString(novaCategoria.getPreco()[1]));
        jTextField4.setText(Float.toString(novaCategoria.getPreco()[2]));
        jTextField5.setText(Float.toString(novaCategoria.getPreco()[3]));
        jTextField6.setText(Float.toString(novaCategoria.getPreco()[4]));
        
    }//GEN-LAST:event_categoriasListMouseClicked

    private void salvarBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTNActionPerformed

        if(!categoriasList.isSelectionEmpty())
            novaCategoria = categorias.get(categoriasList.getSelectedIndex());

        novaCategoria.setNome(nomeTF.getText());
        Float [] precos = new Float []
        {Float.parseFloat(jTextField2.getText()),
            Float.parseFloat(jTextField3.getText()),
            Float.parseFloat(jTextField4.getText()),
            Float.parseFloat(jTextField5.getText()),
            Float.parseFloat(jTextField6.getText())      
        };
        
        novaCategoria.setPreco(precos);
        
        novaCategoria.setId(new CategoriaDao().salvar(novaCategoria));

        limparCampos();
        buscarTodos();
    }//GEN-LAST:event_salvarBTNActionPerformed

    private void excluirBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirBTNActionPerformed
        if(categoriasList.getSelectedValue() == null)
        return;
        novaCategoria = categorias.get(categoriasList.getSelectedIndex());
        new CategoriaDao().excluir(novaCategoria);
        buscarTodos();
        limparCampos();
    }//GEN-LAST:event_excluirBTNActionPerformed

    private void limparBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_limparBTNActionPerformed
        categoriasList.clearSelection();
        novaCategoria = new Categoria(); 
    }//GEN-LAST:event_limparBTNActionPerformed

